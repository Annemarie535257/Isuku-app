// Translation Manager - Simple and Effective
const TranslationManager = {
    currentLanguage: 'en',
    elementMap: new Map(), // Store element and its translation key mapping
    observer: null,
    
    init() {
        // Get language from storage or cookie (cookie takes precedence for cross-page persistence)
        const storedLang = localStorage.getItem('preferredLanguage');
        const cookieLang = this.getCookie('django_language');
        this.currentLanguage = cookieLang || storedLang || 'en';
        
        if (!translations[this.currentLanguage]) {
            this.currentLanguage = 'en';
        }
        
        // Save to both localStorage and cookie for persistence across pages
        localStorage.setItem('preferredLanguage', this.currentLanguage);
        this.setCookie('django_language', this.currentLanguage, 365); // Store for 1 year
        document.documentElement.lang = this.currentLanguage;
        
        // Wait for DOM to be fully ready, then apply translations
        const applyTranslations = () => {
            // Small delay to ensure all elements are rendered
            setTimeout(() => {
                this.applyTranslations();
                this.observeDomMutations();
            }, 50);
        };
        
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', applyTranslations);
        } else {
            applyTranslations();
        }
    },
    
    getCookie(name) {
        const value = `; ${document.cookie}`;
        const parts = value.split(`; ${name}=`);
        if (parts.length === 2) return parts.pop().split(';').shift();
        return null;
    },
    
    setCookie(name, value, days = 365) {
        const date = new Date();
        date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
        document.cookie = `${name}=${value};expires=${date.toUTCString()};path=/`;
    },
    
    translate(key) {
        if (!key) return key;
        const lang = this.currentLanguage;
        return translations[lang]?.[key] || translations['en']?.[key] || key;
    },
    
    setLanguage(lang) {
        if (!translations[lang]) {
            console.error('Invalid language:', lang);
            return false;
        }
        
        console.log('🌍 Changing language to:', lang);
        this.currentLanguage = lang;
        
        // Save to both localStorage and cookie for cross-page persistence
        localStorage.setItem('preferredLanguage', lang);
        this.setCookie('django_language', lang, 365); // Store for 1 year
        document.documentElement.lang = lang;
        
        // Apply translations immediately
        this.applyTranslations();
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
        return true;
    },

    normalizeText(text) {
        return (text || '').replace(/\s+/g, ' ').trim();
    },

    getTranslationForText(text) {
        const key = this.normalizeText(text);
        if (!key) return null;
        const value = this.translate(key);
        return value && value !== key ? value : null;
    },

    shouldSkipElement(element) {
        if (!element || !element.tagName) return true;
        const tag = element.tagName;
        if (['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE'].includes(tag)) return true;
        if (element.closest('[data-no-translate="true"]')) return true;
        if (element.classList && element.classList.contains('notranslate')) return true;
        return false;
    },
    
    applyTranslations() {
        console.log('🔄 Applying translations for:', this.currentLanguage);
        const elements = document.querySelectorAll('[data-translate]');
        console.log(`Found ${elements.length} translatable elements`);
        
        let translated = 0;
        let skipped = 0;
        
        elements.forEach((element, idx) => {
            const key = element.getAttribute('data-translate');
            if (!key) {
                skipped++;
                return;
            }
            
            const translation = this.translate(key);
            if (!translation || translation === key) {
                skipped++;
                return;
            }
            
            try {
                // Handle different element types
                if (element.tagName === 'INPUT') {
                    if (element.type === 'submit' || element.type === 'button') {
                        element.value = translation;
                        translated++;
                    }
                } else if (element.tagName === 'TEXTAREA') {
                    // Placeholder handled separately
                } else if (element.tagName === 'OPTION') {
                    // Handle select option elements
                    if (element.value === '') {
                        // Only translate the placeholder option (empty value)
                        element.textContent = translation;
                        translated++;
                    }
                } else {
                    // Text elements - replace content
                    this.replaceElementText(element, key, translation);
                    translated++;
                }
            } catch (error) {
                console.error(`Error translating element ${idx}:`, error);
            }
        });
        
        // Handle placeholders
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            const translation = this.translate(key);
            if (translation && translation !== key) {
                element.placeholder = translation;
                translated++;
            }
        });

        // Handle aria-label translations
        document.querySelectorAll('[data-translate-aria-label]').forEach(element => {
            const key = element.getAttribute('data-translate-aria-label');
            const translation = this.translate(key);
            if (translation && translation !== key) {
                element.setAttribute('aria-label', translation);
                translated++;
            }
        });

        // Translate input placeholders and values even without explicit attributes.
        document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(element => {
            const originalPlaceholder = element.getAttribute('data-original-placeholder') || element.placeholder;
            if (!element.getAttribute('data-original-placeholder')) {
                element.setAttribute('data-original-placeholder', originalPlaceholder || '');
            }
            const placeholderTranslation = this.getTranslationForText(originalPlaceholder);
            if (placeholderTranslation) {
                element.placeholder = placeholderTranslation;
            }
        });

        document.querySelectorAll('input[type="submit"], input[type="button"], button').forEach(element => {
            if (this.shouldSkipElement(element)) return;

            // Value-based controls
            if (element.tagName === 'INPUT' && element.value) {
                const originalValue = element.getAttribute('data-original-value') || element.value;
                if (!element.getAttribute('data-original-value')) {
                    element.setAttribute('data-original-value', originalValue);
                }
                const valueTranslation = this.getTranslationForText(originalValue);
                if (valueTranslation) {
                    element.value = valueTranslation;
                }
                return;
            }

            // Text button controls without data-translate.
            if (!element.hasAttribute('data-translate')) {
                this.autoTranslateElementText(element);
            }
        });

        // Translate plain text elements that do not explicitly declare data-translate.
        document.querySelectorAll('h1, h2, h3, h4, h5, h6, p, a, span, label, th, td, li, small, strong, div').forEach(element => {
            if (this.shouldSkipElement(element)) return;
            if (element.hasAttribute('data-translate')) return;
            if (element.children.length > 0 && element.querySelector('[data-translate]')) return;
            this.autoTranslateElementText(element);
        });

        // Translate page title if translation key exists.
        if (document && document.title) {
            const titleTranslation = this.getTranslationForText(document.title);
            if (titleTranslation) {
                document.title = titleTranslation;
            }
        }
        
        console.log(`✅ Translated ${translated} elements, skipped ${skipped}`);
    },

    autoTranslateElementText(element) {
        if (!element) return;
        const currentText = this.normalizeText(element.textContent);
        if (!currentText) return;

        // Prevent reusing translated text as source key.
        if (!element.getAttribute('data-original-text')) {
            element.setAttribute('data-original-text', currentText);
        }

        const sourceText = element.getAttribute('data-original-text') || currentText;
        const translatedText = this.getTranslationForText(sourceText);
        if (!translatedText) return;

        this.replaceElementText(element, sourceText, translatedText);
    },

    observeDomMutations() {
        if (this.observer) return;

        this.observer = new MutationObserver((mutations) => {
            let shouldReapply = false;
            for (const mutation of mutations) {
                if (mutation.type === 'childList' && mutation.addedNodes.length > 0) {
                    shouldReapply = true;
                    break;
                }
                if (mutation.type === 'characterData') {
                    shouldReapply = true;
                    break;
                }
            }

            if (shouldReapply) {
                this.applyTranslations();
            }
        });

        this.observer.observe(document.body, {
            childList: true,
            subtree: true,
            characterData: true,
        });
    },
    
    replaceElementText(element, key, translation) {
        // Get current content
        const currentText = element.textContent || '';
        const hasChildren = element.children.length > 0;
        const icons = element.querySelectorAll('i');
        const hasBr = element.innerHTML.includes('<br>') || element.innerHTML.includes('<BR>');
        const spans = element.querySelectorAll('span[data-translate]');
        
        // Case 1: Element has nested spans with data-translate (like the title)
        if (spans.length > 0) {
            // Don't translate parent, let children handle it
            return;
        }
        
        // Case 2: Element has icons
        if (icons.length > 0) {
            // Preserve icons, replace text
            const iconBefore = element.firstElementChild?.tagName === 'I';
            const iconAfter = element.lastElementChild?.tagName === 'I';
            const iconHTML = Array.from(icons).map(icon => icon.outerHTML).join(' ');
            
            if (iconBefore) {
                element.innerHTML = iconHTML + ' ' + translation;
            } else if (iconAfter) {
                element.innerHTML = translation + ' ' + iconHTML;
            } else {
                // Icons in middle - replace text nodes only
                this.replaceTextNodesOnly(element, translation);
            }
        }
        // Case 3: Element has <br> tags
        else if (hasBr) {
            // Check if translation should have line breaks
            if (translation.includes('\n')) {
                element.innerHTML = translation.replace(/\n/g, '<br>');
            } else {
                // Replace text but preserve <br> structure
                const brCount = (element.innerHTML.match(/<br\s*\/?>/gi) || []).length;
                if (brCount > 0 && key.includes('\n')) {
                    const lines = translation.split('\n');
                    element.innerHTML = lines.join('<br>');
                } else {
                    element.textContent = translation;
                }
            }
        }
        // Case 4: Simple text element
        else {
            element.textContent = translation;
        }
    },
    
    replaceTextNodesOnly(element, translation) {
        // Replace only text nodes, preserve all HTML elements
        const walker = document.createTreeWalker(
            element,
            NodeFilter.SHOW_TEXT,
            {
                acceptNode: (node) => {
                    // Skip text inside script, style, or icon elements
                    let parent = node.parentNode;
                    while (parent && parent !== element) {
                        if (['SCRIPT', 'STYLE', 'I'].includes(parent.tagName)) {
                            return NodeFilter.FILTER_REJECT;
                        }
                        parent = parent.parentNode;
                    }
                    return NodeFilter.FILTER_ACCEPT;
                }
            },
            false
        );
        
        const textNodes = [];
        let node;
        while (node = walker.nextNode()) {
            if (node.textContent.trim().length > 0) {
                textNodes.push(node);
            }
        }
        
        if (textNodes.length > 0) {
            // Replace first substantial text node
            textNodes[0].textContent = translation;
            // Clear other text nodes that match the original
            for (let i = 1; i < textNodes.length; i++) {
                const text = textNodes[i].textContent.trim();
                if (text.length > 0 && text.length < 50) {
                    textNodes[i].textContent = '';
                }
            }
        } else {
            // Fallback
            element.textContent = translation;
        }
    },
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
};

// Initialize
(function() {
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', () => {
            TranslationManager.init();
        });
    } else {
        TranslationManager.init();
    }
})();

// Export
window.TranslationManager = TranslationManager;
